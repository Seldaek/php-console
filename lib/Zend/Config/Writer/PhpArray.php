class PhpArray extends AbstractWriter
{
    /**
     * processConfig(): defined by AbstractWriter.
     *
     * @param  array $config
     * @return string
     */
    public function processConfig(array $config)
    {

        $arrayString = "<?php\n"
                     . "return " . var_export($config, true) . ";\n";

        return $arrayString;
    }
}
